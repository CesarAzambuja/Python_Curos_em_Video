#010 Crie um programa leia quanto dinheiro a pessoa tem na carteira e mestre quantos dolares ela pode comprar US$3,27
n1 = float(input('Quantos reias você tem de dinheiro na carteita: '))
n2 = 3.27
n3 = n1/n2
print ('Com esse valor R$ {:.2f} você pode comprar US$ {:.2f} na data de hoje'.format(n1, n3))
